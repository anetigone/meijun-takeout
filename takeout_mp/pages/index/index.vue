			addCart(item, event) {
				try {
					// 查找购物车中是否已有该菜品
					const index = this.cartItems.findIndex(cartItem => cartItem.id === item.id);
					
					if (index >= 0) {
						// 已有该菜品，增加数量
						this.cartItems[index].number += 1;
					} else {
						// 没有该菜品，添加新项
						this.cartItems.push({
							id: item.id,
							name: item.name,
							price: item.price,
							image: item.image,
							number: 1,
							categoryId: item.categoryId
						});
					}
					
					// 保存到本地存储并更新总计
					this.saveCartToStorage();
					this.calculateCartTotals();
					
					// 向后端同步数据（即使失败，本地存储也已更新）
					addCartApi({
						itemId: item.id,
						itemType: "DISH", // 默认为菜品类型
						quantity: 1
					}).catch(err => {
						console.warn('API同步购物车失败，但本地购物车已更新');
					});
					
					// 可以添加添加成功的视觉反馈
					uni.$showMsg('已添加到购物车');
				} catch (e) {
					console.error("添加到购物车失败", e);
					uni.$showMsg('添加失败，请重试');
				}
			},
			
			// 从购物车中减少菜品
			subtractCart(item) {
				try {
					const index = this.cartItems.findIndex(cartItem => cartItem.id === item.id);
					
					if (index >= 0) {
						if (this.cartItems[index].number > 1) {
							// 数量大于1，减少数量
							this.cartItems[index].number -= 1;
					} else {
							// 数量为1，从购物车移除
							this.cartItems.splice(index, 1);
						}
						
						// 保存到本地存储并更新总计
						this.saveCartToStorage();
						this.calculateCartTotals();
						
						// 向后端同步数据
						updateCartApi({
							id: item.id,
							quantity: index >= 0 ? this.cartItems[index]?.number || 0 : 0
						}).catch(err => {
							console.warn('API同步购物车失败，但本地购物车已更新');
						});
					}
				} catch (e) {
					console.error("从购物车移除失败", e);
					uni.$showMsg('操作失败，请重试');
				}
			},
			
